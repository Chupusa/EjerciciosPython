# Operadores relacionales

# Operador menor que
resultado = 10 < 20
print ("El resultado de Operador Menor que es ", resultado)

# Operador mayor que
resultado = 10 > 20
print ("El resultado de Operador Mayor que es ", resultado)

# Operador de Igualdad
resultado = 10 == 20
print ("El resultado de Operador Igualdad es ", resultado)

# Operador de Diferencia
resultado = 10 != 10
print ("El resultado de Operador Diferencia es ", resultado)